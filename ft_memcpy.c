#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*d;
	const unsigned char	*s;

	d = dest;
	s = src;
	if (!dest && !src)
		return (0);
	while (n--)
	{
		*d = *s;
		d++;
		s++;
	}
	return (dest);
}
